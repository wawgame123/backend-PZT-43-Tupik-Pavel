<?php
// --- АРИФМЕТИЧЕСКИЕ ОПЕРАТОРЫ ---
$x = 10; // Присваивание
$y = 3;
$sum = $x + $y; // Сложение
$sub = $x - $y; // Вычитание
$mult = $x * $y; // Умножение
$div = $x / $y; // Деление
$mod = $x % $y; // Остаток от деления (10 на 3 = 1 в остатке)
$exp = $x ** $y; // Возведение в степень (10 в кубе)
// --- ОПЕРАТОРЫ СРАВНЕНИЯ ---
$isEqual = ($x == 10); // Сравнение по значению
$isStrict = ($x === "10"); // Тождественное сравнение (тип + значение) — будет false
$isGreater = ($x > $y); // Больше
// --- ЛОГИЧЕСКИЕ ОПЕРАТОРЫ ---
$and = ($x > 5 && $y < 5); // Логическое И (оба условия верны)
$or = ($x == 10 || $y == 10); // Логическое ИЛИ (хотя бы одно верно)
// --- ИНКРЕМЕНТ И ДЕКРЕМЕНТ ---
$x++; // Увеличение на 1
$y--; // Уменьшение на 1
// --- УСЛОВНЫЙ ОПЕРАТОР (IF-ELSE) ---
if ($x > 5) {
    // Выполнится, если условие истинно
    $status = "Больше пяти";
} else {
    // Выполнится, если ложно
    $status = "Меньше или равно пяти";
}
// --- ЦИКЛ WHILE ---
// Проверяет условие ПЕРЕД выполнением тела цикла
$i = 0;
while ($i < 3) {
    $i++; // Увеличиваем счетчик, чтобы не зациклиться
}
// --- ЦИКЛ DO-WHILE ---
// Выполняет тело цикла ХОТЯ БЫ ОДИН РАЗ, даже если условие ложно
$j = 10;
do {
    $j++; 
} while ($j < 5); // Условие сразу ложно, но один шаг будет сделан
// --- ЦИКЛ FOR ---
// Удобен, когда мы заранее знаем количество повторений
for ($k = 0; $k < 5; $k++) {
    // k стартует с 0, работает пока k < 5, после каждого круга k увеличивается на 1
    $temp = $k * 2;
}